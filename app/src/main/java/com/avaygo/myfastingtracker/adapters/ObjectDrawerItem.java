public class ObjectDrawerItem {

    public int icon;
    public String name;

    public ObjectDrawerItem() {
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getIcon() {
        return icon;
    }

    public String getName() {
        return name;
    }
}
